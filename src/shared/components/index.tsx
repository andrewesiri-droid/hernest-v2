import React from "react";
import { T, F, SHADOWS } from "../../config/theme";

// ── Card ──────────────────────────────────────────────────────────
export function Card({
  children,
  style,
  onClick,
}: {
  children: React.ReactNode;
  style?: React.CSSProperties;
  onClick?: () => void;
}) {
  return (
    <div
      onClick={onClick}
      style={{
        background: T.ivory,
        borderRadius: 20,
        border: `1px solid ${T.linen}`,
        padding: "16px 18px",
        boxShadow: SHADOWS.sm,
        marginBottom: 12,
        cursor: onClick ? "pointer" : "default",
        ...style,
      }}
    >
      {children}
    </div>
  );
}

// ── HeroCard ──────────────────────────────────────────────────────
export function HeroCard({
  eyebrow,
  title,
  subtitle,
  children,
  color = T.esp,
}: {
  eyebrow?: string;
  title: string;
  subtitle?: string;
  children?: React.ReactNode;
  color?: string;
}) {
  return (
    <div
      style={{
        background: `linear-gradient(135deg, ${color} 0%, ${color}dd 100%)`,
        borderRadius: 24,
        padding: "22px 20px",
        marginBottom: 16,
        boxShadow: SHADOWS.lg,
      }}
    >
      {eyebrow && (
        <p style={{ fontFamily: F.sans, fontSize: 10, fontWeight: 600, letterSpacing: "0.16em", textTransform: "uppercase", color: "rgba(255,255,255,0.5)", margin: "0 0 8px" }}>
          {eyebrow}
        </p>
      )}
      <h2 style={{ fontFamily: F.serif, fontStyle: "italic", fontSize: 26, fontWeight: 500, color: "#fff", margin: "0 0 4px", lineHeight: 1.2 }}>
        {title}
      </h2>
      {subtitle && (
        <p style={{ fontFamily: F.sans, fontSize: 13, color: "rgba(255,255,255,0.65)", margin: "6px 0 0" }}>
          {subtitle}
        </p>
      )}
      {children}
    </div>
  );
}

