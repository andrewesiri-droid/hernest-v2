// ── Input ─────────────────────────────────────────────────────────
export function Input({
  value,
  onChange,
  placeholder,
  type = "text",
  style,
}: {
  value: string;
  onChange: (v: string) => void;
  placeholder?: string;
  type?: string;
  style?: React.CSSProperties;
}) {
  return (
    <input
      type={type}
      value={value}
      onChange={(e) => onChange(e.target.value)}
      placeholder={placeholder}
      style={{
        width: "100%",
        background: T.ivory,
        border: `1.5px solid ${T.linen}`,
        borderRadius: 12,
        padding: "12px 14px",
        fontFamily: F.sans,
        fontSize: 16, // 16px prevents iOS zoom on focus
        color: T.esp,
        outline: "none",
        boxSizing: "border-box",
        WebkitAppearance: "none",
        ...style,
      }}
    />
  );
}

// ── Button ────────────────────────────────────────────────────────
export function Button({
  children,
  onClick,
  variant = "primary",
  disabled,
  style,
}: {
  children: React.ReactNode;
  onClick?: () => void;
  variant?: "primary" | "secondary" | "ghost" | "gold";
  disabled?: boolean;
  style?: React.CSSProperties;
}) {
  const styles: Record<string, React.CSSProperties> = {
    primary:   { background: T.esp, color: "#fff", border: "none" },
    secondary: { background: T.sand, color: T.esp, border: `1px solid ${T.linen}` },
    ghost:     { background: "none", color: T.bark, border: `1px solid ${T.linen}` },
    gold:      { background: `linear-gradient(135deg, ${T.gold}, #a88420)`, color: "#fff", border: "none" },
  };

  return (
    <button
      onClick={onClick}
      disabled={disabled}
      style={{
        ...styles[variant],
        borderRadius: 14,
        padding: "14px 20px",
        fontFamily: F.sans,
        fontSize: 14,
        fontWeight: 600,
        cursor: disabled ? "not-allowed" : "pointer",
        opacity: disabled ? 0.5 : 1,
        transition: "all .2s",
        width: "100%",
        minHeight: 48,
        touchAction: "manipulation",
        WebkitTapHighlightColor: "transparent",
        ...style,
      }}
    >
      {children}
    </button>
  );
}

// ── ProgressBar ───────────────────────────────────────────────────
export function ProgressBar({
  value,
  max = 100,
  color = T.gold,
  height = 6,
}: {
  value: number;
  max?: number;
  color?: string;
  height?: number;
}) {
  const pct = Math.min((value / max) * 100, 100);
  return (
    <div style={{ background: `${color}20`, borderRadius: 99, height, overflow: "hidden" }}>
      <div style={{
        width: `${pct}%`, height: "100%",
        background: color, borderRadius: 99,
        transition: "width .6s cubic-bezier(.4,0,.2,1)",
      }} />
    </div>
  );
}

